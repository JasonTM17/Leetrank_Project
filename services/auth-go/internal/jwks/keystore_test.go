// TestSignVerify_Roundtrip ensures EdDSA-signed tokens decode against
// the same keystore. Catches signer/verifier drift.
func TestSignVerify_Roundtrip(t *testing.T) {
	ks, err := New("", nil)
	if err != nil {
		t.Fatalf("New: %v", err)
	}
	tok, err := ks.Sign("user-42", "user", "leetrank", 5*time.Minute)
	if err != nil {
		t.Fatalf("Sign: %v", err)
	}
	claims, err := ks.Verify(tok, "leetrank")
	if err != nil {
		t.Fatalf("Verify: %v", err)
	}
	if claims.Subject != "user-42" {
		t.Errorf("sub = %q, want user-42", claims.Subject)
	}
	if claims.Role != "user" {
		t.Errorf("role = %q, want user", claims.Role)
	}
}

// TestVerify_RejectsBadAudience prevents token reuse across audiences.
func TestVerify_RejectsBadAudience(t *testing.T) {
	ks, _ := New("", nil)
	tok, _ := ks.Sign("u", "user", "leetrank", time.Minute)
	if _, err := ks.Verify(tok, "leetrank-admin"); err == nil {
		t.Fatal("expected audience error, got nil")
	}
}

// TestVerify_RejectsExpired ensures expired tokens are rejected.
// Uses -10m so the default 1-minute leeway in go-jose still treats
